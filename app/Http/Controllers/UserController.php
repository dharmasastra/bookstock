    public function dataUsers() 
    {
        return Datatables::of(User::where('role', 'STAFF'))
        ->addColumn('action', function ($user) 
        {
            $html = '';
            $html .= '<a href="'.route("users.destroy", ["user"=>$user->id]).'" class="btn btn-xs btn-delete delete">Delete</a>'; 
            
            return $html;
        })
        ->addIndexColumn()
        ->make(true);
    }
